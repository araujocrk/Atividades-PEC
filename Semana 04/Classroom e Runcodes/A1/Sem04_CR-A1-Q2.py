#As variáveis recebem, convertidas para inteiro, os valores digitados pelo usuário
a = int(input('Insira o valor de "a": '))
b = int(input('Insira o valor de "b": '))
c = int(input('Insira o valor de "c": '))
#A variável recebe o resultado do cálculo da média
media = (a + b + c) / 3
#Mostra na tela o resultado da média
print(f'A média desses valores é {media}')
